def custom_openapi():
    """
    Custom function to generate the OpenAPI schema for the API.

    This function is used to customize the OpenAPI schema generated by FastAPI. It checks if the schema has already been generated and returns it if it exists. Otherwise, it calls the `get_openapi` function to generate the schema. It then adds a reusable header parameter named "X-API-Version" with a default value of "1" to every method in every route that starts with "/api/". The modified schema is stored in the `app.openapi_schema` attribute and returned.

    Returns:
        dict: The modified OpenAPI schema.

    """
    if app.openapi_schema:
        return app.openapi_schema

    openapi_schema = get_openapi(
        title=app.title,
        version=app.version,
        routes=app.routes,
    )

    # Define reusable header parameter
    api_version_header = {
        "name": "X-API-Version",
        "in": "header",
        "required": True,
        "schema": {"type": "string", "default": "1"},
    }

    # Inject into every /api/* route + every method
    for path, methods in openapi_schema["paths"].items():
        if path.startswith("/api/"):
            for method in methods.values():
                method.setdefault("parameters", [])
                # avoid duplicates
                if not any(
                    p.get("name") == "X-API-Version" for p in method["parameters"]
                ):
                    method["parameters"].append(api_version_header)

    app.openapi_schema = openapi_schema
    return app.openapi_schema


app = FastAPI(title=settings.PROJECT_NAME, version=settings.PROJECT_VERSION)
app.openapi = custom_openapi

