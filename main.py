app.include_router(populators.router, tags=["Populators"], prefix="/api")


@app.exception_handler(HTTPException)
async def custom_http_exception_handler(request: Request, exc: HTTPException):
    """
    Custom exception handler for HTTPException.

    This function is used as an exception handler for HTTPException. It checks if the exception detail is a dictionary and if it contains a "status" key. If so, it returns a JSONResponse with the status code and the exception detail. Otherwise, it returns a JSONResponse with a generic error message.

    Args:
        request (Request): The incoming request.
        exc (HTTPException): The HTTPException instance.

    Returns:
        JSONResponse: The JSONResponse with the appropriate status code and content.

    """
    if isinstance(exc.detail, dict) and "status" in exc.detail:
        return JSONResponse(status_code=exc.status_code, content=exc.detail)
    return JSONResponse(
        status_code=exc.status_code,
        content={"status": "error", "message": str(exc.detail)},
    )


